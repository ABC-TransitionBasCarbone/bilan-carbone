    // cannot edit its own rights
    cy.getByTestId('study-rights-table-line').eq(0).contains('bc-admin-1@yopmail.comValidateur')
    cy.getByTestId('study-rights-table-line')
      .eq(0)
      .within(() => {
        cy.get('input').should('be.disabled')
      })
